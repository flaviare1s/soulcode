/*
    Operadores relacionais
    
    < Menor que
    > Maior que
    <= Menor ou igual
    >= Maior ou igual
    == Igual a
    === Igual a e o mesmo tipo
    != Diferente
    !== Etsritamente diferente
*/

let a =3;
let b = 5;

console.log(a < b);
console.log(a > b);
console.log(b >= a);

console.log(a != '3'); // false, são iguais em valor
console.log(a !== '3'); // true, são diferentes em tipo

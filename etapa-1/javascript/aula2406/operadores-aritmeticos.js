// OPERADORES ARITMÉTICOS:
  // Soma (+)
  // Subtração (-)
  // Multiplicação (*)
  // Divisão (/)
  // Resto da divisão (%)
  // Potência (**)

const num1 = 5;
const num2 = 8;
const num3 = 4.5;

let resultado;

// Soma (+)
resultado = num1 + num2 + num3;
console.log(resultado);

// Subtração (-)
resultado = num2 - num1;
console.log(resultado);

// Multiplicação (*)
resultado = num1 * num2;
console.log(resultado);

// Divisão (/)  
resultado = num2 / num1;
console.log(resultado);

// Resto da divisão (%)
resultado = num2 % num1;
console.log(resultado);

// Potência (**)  
resultado = num1 ** 2;
console.log(resultado);
